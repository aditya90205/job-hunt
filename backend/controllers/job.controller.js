export const getJobById = async (req, res) => {
  try {
    const jobId = req.params.id;
    const job = await Job.findById(jobId).populate({
      path: "applications",
    });
    if (!job) {
      return res.status(404).json({
        message: "Jobs not found.",
        success: false,
      });
    }
    // Only allow viewing unapproved job details to superadmin or the job creator
    if (job.isApprove === false) {
      const requester = await User.findById(req.id).select("role");
      if (!requester) {
        return res
          .status(403)
          .json({ message: "Not authorized to view this job", success: false });
      }
      const isCreator = String(job.created_by) === String(req.id);
      if (requester.role !== "superadmin" && !isCreator) {
        return res
          .status(403)
          .json({
            message: "This job is pending admin approval",
            success: false,
          });
      }
    }
    return res.status(200).json({ job, success: true });
  } catch (error) {
    console.log(error);
  }
};
